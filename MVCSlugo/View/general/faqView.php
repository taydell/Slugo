    <p><strong>1.) What Is SLUGO?</strong></p>
    <p>Answer: SLUGO is where you want to be! SLUGO is a bi-annual, 24-hour  gaming convention, hosted by the SELU ACM.
        Every semester, gamers look forward  to this premiere event. &nbsp;It’s your chance  to show your skills in both
        PC and console tournaments, featuring some of the  newest and hottest titles, and some of those timeless favorites. </p>
    <hr>
    <p><strong>2.) Where is SLUGO held?</strong></p>
    <p>Answer: Typically SLUGO is held in the War Memorial Student  Union on the SELU campus in Hammond,   LA.</p>
    <hr>
    <p><strong>3.) When is SLUGO held?</strong></p>
    <p>Answer: Dates will vary due to the ever changing college  schedules, but generally SLUGO is held near the SELU term’s
        class drop date, usually  on that Friday.</p>
    <hr>
    <p><strong>4.) Is SLUGO free?</strong></p>
    <p>Answer: No. It takes an extreme amount of effort to organize,  plan, and run SLUGO. In the past the SELU ACM has paid
        out of pocket to host  SLUGO, diminishing it’s funds substantially.</p>
    <hr>
    <p><strong>5.) OK… Not free, I get it. How much is it then?</strong></p>
    <p>Answer: Generally $5.00 a person. (This can change)</p>
    <hr>
    <p><strong>6.) What should I bring?</strong></p>
    <p>Answer: SLUGO supports a 60 spot BYOC (Bring Your Own  Computer). If you wish to participate in PC tournaments you must
        bring a  computer with a NIC (Network Interface Card). You will also need a monitor,  keyboard, mouse, and any cables
        (power, usb, etc…). You may also wish to bring  a headset (No speakers! Ever ‘hear’ of noise pollution? ^_^ ) and a surge
        protector or power strip. Additionally, it is a good idea to bring the game  discs and serial keys of the games you will be
        playing (Just in case).</p>
    <p>If you are not participating in PC tournaments then a  computer is not required.</p>
    <p>You must bring enough money ($5.00 cash) to cover the  entrance fee, but it’s a good idea to bring enough to buy food.
        Gaming and  hunger never mix well.</p>
    <p>Because SLUGO is a 24-hour gaming convention it is  recommended that gamers take a break from the fun to grab a few Zzz’s. The
        floor isn’t very comfortable, so a sleeping bag may come in handy.</p>
    <hr>
    <p><strong>7.) What shouldn’t I bring?</strong></p>
    <p>Answer: Alcohol, weapons, drugs, and pornography are  strictly forbidden. If you bring any of these items we will escort you from
        the  event. Speakers are not allowed, as they produce noise pollution. Also, cheating  programs (hack-bots, cheat-scripts, etc…)
        will have you banned from  tournaments.</p>
    <hr>
    <p><strong>8.) What games are played at SLUGO?</strong></p>
    <p>Answer: Usually, Counter Strike, Unreal Tournament 2004, and  Team Fortress 2 are played, but it depends on the crowd. Have an idea?
        Bring it  up at an ACM meeting!</p>
    <hr>
    <p><strong>9.) I’m underage, can I attend SLUGO?</strong></p>
    <p>Answer: Yes, however a parent/guardian must sign a release  form. You must
        present this signed release form at the door when you pay the entrance fee.</p>
    <hr>
    <p><strong>10.) Do I have to be a student to attend SLUGO?</strong></p>
    <p>Answer: No, anyone may attend SLUGO.</p>
    <hr>
    <p><strong>11.) Wow! I want to go to SLUGO! How do I sign up?</strong></p>
    <p>Answer: Woah, hold on! If you are not participating in any  PC tournaments then all you need to do is come to the event. (Console
        tournaments may require advanced registration) </p>
    <p>If you plan to bring a computer then register your machine online.</p>
